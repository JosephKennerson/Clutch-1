def fffaker_event
    my_videogames = ["play Mario Party!", "play Halo!", "play Super Smash Brothers Melee!", "play FIFA!", "play League of Legends!", "play Hearthstone!"]
    my_boardgames = ["play Catan!", "play Monopoly!", "play Cards Against Humanity!", "play Risk: Legacy!", "play King of Tokyo!", "play Pandemic!"]
    my_basketball = ["play horse!", "play some pickup basketball!", "play half court ball.", "shoot around a little."]
    my_soccer = ["skirmish for a while!", "play around the world.", "get some shooting practice.", "play soccer!"]
    my_tennis = ['play tennis!', 'play doubles tennis.', 'practice rallying.']
    my_cycling = ['race!', 'go for a leisurely ride.', 'do a charity ride.']
    my_hiking = ['go hiking nearby.', 'hike up the local mountains!', 'hike right here in our backyard,', "go hiking if you're an exapt!"]
    my_restaurant = ['try this new restaurant!', 'eat dinner at this local favorite joint.', 'do a group brunch.', 'make friends while we break bread!']
    my_other = ['marathon the new season of House of Cards.', 'go on an ice cream binge!', 'come over and play beerpong!', 'hit the gym.', 'go surfing!', 'check out the new exhibit at the California Science Museum.']

    my_first_phrase = ["Let's ", "Looking for awesome people to ", "Hey guys, let's ", "Anyone welcome to ", "Let's hangout and ", "Come and ", "Celebrating funemployment! Let's "]
    my_category = ["videogames", "boardgames", "basketball", "soccer", "tennis", "cycling", "hiking", "restaurant exploring", "other"].sample
    my_name = ""

    case my_category
        when "videogames"
            my_name = my_first_phrase.sample + my_videogames.sample
        when "boardgames"
            my_name = my_first_phrase.sample + my_boardgames.sample
        when "basketball"
            my_name = my_first_phrase.sample + my_basketball.sample
        when "soccer"
            my_name = my_first_phrase.sample + my_soccer.sample
        when "tennis"
            my_name = my_first_phrase.sample + my_tennis.sample
        when "cycling"
            my_name = my_first_phrase.sample + my_cycling.sample
        when "hiking"
            my_name = my_first_phrase.sample + my_hiking.sample
        when "restaurant exploring"
            my_name = my_first_phrase.sample + my_restaurant.sample
        else
            my_name = my_first_phrase.sample + my_other.sample
    end

    {category: my_category, name: my_name}
end

